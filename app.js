
///////// First Question
const arrowDown1 = document.querySelector('.arrow-down-one');
const arrowUp1 = document.querySelector('.arrow-up-one');
const faqAnswer1 = document.getElementById('faq-answer-one');

arrowDown1.addEventListener('click', () => {
  arrowDown1.style.display = 'none';
  arrowUp1.style.display = 'block';
  faqAnswer1.style.display = 'block';
}) 

arrowUp1.addEventListener('click', () => {
  arrowUp1.style.display = 'none';
  arrowDown1.style.display = 'block';
  faqAnswer1.style.display = 'none';
})


